# frozen_string_literal: true

require "rails_helper"

# Verifies that the node_modules controller's strong_params actually
# accept the full spec/lifecycle field surface — including the new
# `protected_spec` and the previously-untyped `package_spec /
# dependency_spec / lock_spec / init_*` fields.
#
# Pre-2026-05-02, the controller permitted only `mask: {}` and
# `file_spec: {}` — a Hash shape that silently dropped Array inputs
# and ignored the other spec fields entirely. This spec is the gate
# against that regression coming back.
RSpec.describe "Module spec field updates", type: :request do
  let(:account)  { create(:account) }
  let(:platform) { create(:system_node_platform, account: account) }
  let(:category) { create(:system_node_module_category, account: account) }
  let(:operator) { user_with_permissions("system.modules.update", "system.modules.read", account: account) }
  let(:mod) do
    create(:system_node_module, account: account, node_platform: platform,
           category: category, variety: "subscription", name: "spec-test-mod")
  end

  describe "PATCH /api/v1/system/node_modules/:id" do
    it "accepts spec fields as scalar newline-joined strings" do
      patch "/api/v1/system/node_modules/#{mod.id}",
            params: {
              node_module: {
                mask:            "/var/cache/apt/**\n/usr/share/doc/**",
                file_spec:       "/etc/foo/**\n/usr/bin/foo",
                package_spec:    "foo\nfoo-extras",
                dependency_spec: "/etc/foo/dep",
                protected_spec:  "/etc/foo/secret"
              }
            }.to_json,
            headers: auth_headers_for(operator).merge("Content-Type" => "application/json")

      expect(response).to have_http_status(:ok)
      mod.reload
      expect(mod.mask_text).to            eq("/usr/share/doc/**\n/var/cache/apt/**\n")
      expect(mod.file_spec_text).to       eq("/etc/foo/**\n/usr/bin/foo\n")
      expect(mod.package_spec_text).to    eq("foo\nfoo-extras\n")
      expect(mod.dependency_spec_text).to eq("/etc/foo/dep\n")
      expect(mod.protected_spec_text).to  eq("/etc/foo/secret\n")
    end

    it "accepts spec fields as already-encoded Arrays" do
      encoded_mask     = [ "/etc/already_encoded" ].map { |l| Base64.strict_encode64(l) }
      encoded_protect  = [ "/etc/programmatic_claim" ].map { |l| Base64.strict_encode64(l) }

      patch "/api/v1/system/node_modules/#{mod.id}",
            params: {
              node_module: {
                mask:            encoded_mask,
                protected_spec:  encoded_protect
              }
            }.to_json,
            headers: auth_headers_for(operator).merge("Content-Type" => "application/json")

      expect(response).to have_http_status(:ok)
      mod.reload
      # Array form should round-trip without re-encoding.
      expect(mod.read_attribute(:mask)).to            eq(encoded_mask)
      expect(mod.read_attribute(:protected_spec)).to  eq(encoded_protect)
      # And decode_spec_text round-trips back to the originals.
      expect(mod.mask_text).to            eq("/etc/already_encoded\n")
      expect(mod.protected_spec_text).to  eq("/etc/programmatic_claim\n")
    end

    it "accepts lifecycle + lock fields" do
      patch "/api/v1/system/node_modules/#{mod.id}",
            params: {
              node_module: {
                init_start:      "systemctl start foo",
                init_stop:       "systemctl stop foo",
                init_restart:    "systemctl reload foo",
                reboot_required: true,
                lock_spec:       true
              }
            }.to_json,
            headers: auth_headers_for(operator).merge("Content-Type" => "application/json")

      expect(response).to have_http_status(:ok)
      mod.reload
      expect(mod.init_start).to       eq("systemctl start foo")
      expect(mod.init_stop).to        eq("systemctl stop foo")
      expect(mod.init_restart).to     eq("systemctl reload foo")
      expect(mod.reboot_required).to  be true
      expect(mod.lock_spec).to        be true
    end
  end

  describe "POST /api/v1/system/node_modules/:id/import_manifest" do
    let(:valid_yaml) do
      <<~YAML
        schema_version: 1
        name: spec-test-mod
        description: "Imported via controller spec."
        mask:
          - "/var/cache/apt/**"
        file_spec:
          - "/etc/imported/**"
        package_spec:
          - "imported"
        protected_spec:
          - "/etc/imported/secret"
        init:
          start: "systemctl start imported"
          stop:  "systemctl stop imported"
        reboot_required: false
      YAML
    end

    it "imports the manifest and returns the updated module" do
      post "/api/v1/system/node_modules/#{mod.id}/import_manifest",
           params: { yaml: valid_yaml }.to_json,
           headers: auth_headers_for(operator).merge("Content-Type" => "application/json")

      expect(response).to have_http_status(:ok)
      data = JSON.parse(response.body)["data"]
      expect(data["node_module"]["protected_spec_text"]).to include("/etc/imported/secret")
      expect(data["node_module"]["init_start"]).to eq("systemctl start imported")
      expect(data["node_module"]["description"]).to eq("Imported via controller spec.")
    end

    it "creates a NodeModuleVersion when create_version is true" do
      post "/api/v1/system/node_modules/#{mod.id}/import_manifest",
           params: { yaml: valid_yaml, create_version: true, changelog: "First import" }.to_json,
           headers: auth_headers_for(operator).merge("Content-Type" => "application/json")

      expect(response).to have_http_status(:ok)
      data = JSON.parse(response.body)["data"]
      expect(data["node_module_version_id"]).to be_present
      version = System::NodeModuleVersion.find(data["node_module_version_id"])
      expect(version.changelog).to eq("First import")
    end

    it "returns 422 with validation errors for an invalid manifest" do
      bad_yaml = valid_yaml.sub("schema_version: 1", "schema_version: 99")
      post "/api/v1/system/node_modules/#{mod.id}/import_manifest",
           params: { yaml: bad_yaml }.to_json,
           headers: auth_headers_for(operator).merge("Content-Type" => "application/json")

      expect(response).to have_http_status(:unprocessable_entity)
      body = JSON.parse(response.body)
      expect(body["error"]).to include("validation failed")
      expect(body.dig("details", "validation_errors").join).to include("schema_version")
    end

    it "returns 400 when no yaml is provided" do
      post "/api/v1/system/node_modules/#{mod.id}/import_manifest",
           params: {}.to_json,
           headers: auth_headers_for(operator).merge("Content-Type" => "application/json")
      expect(response).to have_http_status(:bad_request)
    end
  end

  describe "GET /api/v1/system/node_modules/:id" do
    it "exposes all five spec fields and their _text companions" do
      mod.update!(
        mask:            "/etc/secret",
        file_spec:       "/etc/foo",
        package_spec:    "foo",
        dependency_spec: "/etc/dep",
        protected_spec:  "/etc/claim",
        init_start:      "systemctl start foo",
        lock_spec:       true,
        reboot_required: true
      )

      get "/api/v1/system/node_modules/#{mod.id}",
          headers: auth_headers_for(operator)

      expect(response).to have_http_status(:ok)
      data = JSON.parse(response.body).dig("data", "node_module")
      expect(data).to include(
        "mask",            "mask_text",
        "file_spec",       "file_spec_text",
        "package_spec",    "package_spec_text",
        "dependency_spec", "dependency_spec_text",
        "protected_spec",  "protected_spec_text",
        "lock_spec", "init_start", "init_stop", "init_restart",
        "reboot_required"
      )
      expect(data["protected_spec_text"]).to eq("/etc/claim\n")
      expect(data["lock_spec"]).to be true
      expect(data["reboot_required"]).to be true
      expect(data["init_start"]).to eq("systemctl start foo")
    end
  end
end
