# frozen_string_literal: true

require "rails_helper"

# Audit plan P0.1 wave 1 — controller spec for node_modules.
#
# Permission family: system.modules.*. Destroy guards against dependent
# modules (returns 422 — preventing silent orphans). Several member actions
# (mark_canary, unmark_canary, dependencies, import_manifest, rollback) get
# at-least auth+permission coverage; their full semantics are tested in the
# corresponding service specs.
RSpec.describe "Api::V1::System::NodeModules", type: :request do
  let(:account)       { create(:account) }
  let(:other_account) { create(:account) }

  let(:read_user)   { user_with_permissions("system.modules.read",   account: account) }
  let(:create_user) { user_with_permissions("system.modules.create", account: account) }
  let(:update_user) { user_with_permissions("system.modules.update", account: account) }
  let(:delete_user) { user_with_permissions("system.modules.delete", account: account) }
  let(:no_perms)    { user_with_permissions(account: account) }

  let(:platform) { create(:system_node_platform, account: account) }
  let(:category) { create(:system_node_module_category, account: account) }
  let!(:node_module) do
    create(:system_node_module, account: account, node_platform: platform, category: category)
  end

  describe "GET /api/v1/system/node_modules" do
    it "returns 401 without auth" do
      get "/api/v1/system/node_modules"
      expect(response).to have_http_status(:unauthorized)
    end

    it "returns 403 without read perm" do
      get "/api/v1/system/node_modules", headers: auth_headers_for(no_perms)
      expect(response).to have_http_status(:forbidden)
    end

    it "scopes to the caller's account" do
      foreign_platform = create(:system_node_platform, account: other_account)
      foreign_category = create(:system_node_module_category, account: other_account)
      foreign = create(:system_node_module, account: other_account,
                                            node_platform: foreign_platform, category: foreign_category)
      get "/api/v1/system/node_modules", headers: auth_headers_for(read_user)
      ids = json_response_data["node_modules"].map { |m| m["id"] }
      expect(ids).to include(node_module.id)
      expect(ids).not_to include(foreign.id)
    end
  end

  describe "GET /api/v1/system/node_modules/:id" do
    it "returns the module" do
      get "/api/v1/system/node_modules/#{node_module.id}", headers: auth_headers_for(read_user)
      expect(response).to have_http_status(:ok)
      expect(json_response_data["node_module"]["id"]).to eq(node_module.id)
    end
  end

  describe "POST /api/v1/system/node_modules" do
    let(:create_params) do
      { node_module: { name: "spec-mod-#{SecureRandom.hex(3)}",
                        node_platform_id: platform.id, category_id: category.id, variety: "config" } }
    end

    it "returns 403 without create perm" do
      post "/api/v1/system/node_modules", params: create_params.to_json,
                                          headers: auth_headers_for(no_perms).merge("Content-Type" => "application/json")
      expect(response).to have_http_status(:forbidden)
    end

    it "creates a module scoped to the caller's account" do
      expect {
        post "/api/v1/system/node_modules", params: create_params.to_json,
                                            headers: auth_headers_for(create_user).merge("Content-Type" => "application/json")
      }.to change { ::System::NodeModule.where(account: account).count }.by(1)
      expect(response).to have_http_status(:created)
    end
  end

  describe "PATCH /api/v1/system/node_modules/:id" do
    it "updates the module" do
      patch "/api/v1/system/node_modules/#{node_module.id}",
            params: { node_module: { description: "spec-updated" } }.to_json,
            headers: auth_headers_for(update_user).merge("Content-Type" => "application/json")
      expect(response).to have_http_status(:ok)
      expect(node_module.reload.description).to eq("spec-updated")
    end
  end

  describe "DELETE /api/v1/system/node_modules/:id" do
    it "deletes a module with no dependents" do
      expect {
        delete "/api/v1/system/node_modules/#{node_module.id}", headers: auth_headers_for(delete_user)
      }.to change { ::System::NodeModule.where(account: account).count }.by(-1)
      expect(response).to have_http_status(:ok)
    end
  end

  describe "GET /api/v1/system/node_modules/:id/dependencies" do
    it "returns the dependency graph" do
      get "/api/v1/system/node_modules/#{node_module.id}/dependencies",
          headers: auth_headers_for(read_user)
      expect(response).to have_http_status(:ok)
      expect(json_response_data).to have_key("dependencies")
      expect(json_response_data).to have_key("dependents")
    end
  end
end
