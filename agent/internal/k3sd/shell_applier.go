// WriteJoinConfig writes a systemd Environment= drop-in. K3s' agent
// systemd unit reads K3S_URL + K3S_TOKEN from the environment; the
// drop-in mechanism is the canonical Linux way to override a unit's
// env without editing the unit file.
//
// File format:
//   [Service]
//   Environment="K3S_URL=https://[fd00::1]:6443"
//   Environment="K3S_TOKEN=K10agent-tok"
//
// Atomic write: write to .tmp, rename. Daemon-reload after rename so
// systemd picks up the new env on next start.
func (s *ShellAgentApplier) WriteJoinConfig(ctx context.Context, cfg AgentJoinConfig) error {
	if cfg.APIEndpoint == "" || cfg.AgentToken == "" {
		return errors.New("WriteJoinConfig: APIEndpoint + AgentToken required")
	}
	body := fmt.Sprintf(`[Service]
Environment="K3S_URL=%s"
Environment="K3S_TOKEN=%s"
`, cfg.APIEndpoint, cfg.AgentToken)

	dir := filepath.Dir(s.Paths.AgentEnvFilePath)
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return fmt.Errorf("mkdir %s: %w", dir, err)
	}
	if err := fsutil.AtomicWrite(s.Paths.AgentEnvFilePath, []byte(body), 0o600); err != nil {
		return err
	}
	// Best-effort daemon-reload so the new env is visible before next
	// start. If systemctl fails (test env without systemd), the next
	// `systemctl start` will still work — systemd reads drop-ins on
	// every start.
	_, _ = s.execCmd(ctx, "systemctl", "daemon-reload")
	return nil
}

func (s *ShellAgentApplier) Cleanup(ctx context.Context) error {
	// Remove the env drop-in first (best-effort).
	_ = os.Remove(s.Paths.AgentEnvFilePath)
	// Exec uninstall script if present.
	if _, err := os.Stat(s.Paths.UninstallAgentPath); os.IsNotExist(err) {
		return nil
	}
	_, err := s.execCmd(ctx, s.Paths.UninstallAgentPath)
	return err
}

