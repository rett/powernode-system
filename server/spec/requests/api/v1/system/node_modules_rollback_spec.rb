# frozen_string_literal: true

require "rails_helper"

RSpec.describe "Operator API — Node Modules rollback", type: :request do
  let(:account) { create(:account) }
  let(:other_account) { create(:account) }
  let(:user) { user_with_permissions("system.modules.read", "system.modules.update", account: account) }
  let(:headers) { auth_headers_for(user).merge("Content-Type" => "application/json") }

  let(:platform) { create(:system_node_platform, account: account) }
  let(:category) { create(:system_node_module_category, account: account) }
  let(:node_module) do
    create(:system_node_module, account: account, node_platform: platform,
           category: category, variety: "subscription", name: "test-mod")
  end

  # Stand up two versions deterministically. Using update_columns to bypass
  # NodeModule's after_update :auto_create_version callback — the callback's
  # behavior is exercised elsewhere; here we want explicit control of the
  # version chain so rollback's "go to previous" semantics is unambiguous.
  let!(:v1) do
    v = create(:system_node_module_version, node_module: node_module,
               version_number: 1, file_spec: { "a" => "1" })
    node_module.update_columns(current_version_id: v.id, current_version_number: 1,
                               file_spec: { "a" => "1" })
    v
  end
  let!(:v2) do
    v = create(:system_node_module_version, node_module: node_module,
               version_number: 2, file_spec: { "b" => "2" })
    node_module.update_columns(current_version_id: v.id, current_version_number: 2,
                               file_spec: { "b" => "2" })
    v
  end

  describe "POST /api/v1/system/node_modules/:id/rollback" do
    context "default behavior (no target_version_id)" do
      it "rolls back to the immediately previous version" do
        baseline = node_module.versions.maximum(:version_number)
        post "/api/v1/system/node_modules/#{node_module.id}/rollback", headers: headers
        expect(response).to have_http_status(:ok)

        body = JSON.parse(response.body)
        expect(body.dig("data", "new_version", "version_number")).to eq(baseline + 1)
        expect(body.dig("data", "node_module", "file_spec")).to eq({ "a" => "1" })
      end
    end

    context "with target_version_id" do
      it "rolls back to the specified version" do
        baseline = node_module.versions.maximum(:version_number)

        post "/api/v1/system/node_modules/#{node_module.id}/rollback",
             params: { target_version_id: v1.id }.to_json, headers: headers

        expect(response).to have_http_status(:ok)
        body = JSON.parse(response.body)
        expect(body.dig("data", "new_version", "version_number")).to eq(baseline + 1)
        expect(node_module.reload.file_spec).to eq({ "a" => "1" })
      end

      it "404s when target_version_id is unknown" do
        post "/api/v1/system/node_modules/#{node_module.id}/rollback",
             params: { target_version_id: SecureRandom.uuid }.to_json, headers: headers

        expect(response).to have_http_status(:not_found)
      end
    end

    context "lock state" do
      it "422s when the module is locked" do
        node_module.update!(lock_spec: true)

        post "/api/v1/system/node_modules/#{node_module.id}/rollback", headers: headers

        expect(response).to have_http_status(:unprocessable_content)
        expect(JSON.parse(response.body)["error"]).to match(/locked/i)
      end
    end

    context "cross-account isolation" do
      it "404s for a module in another account" do
        foreign_platform = create(:system_node_platform, account: other_account)
        foreign_cat = create(:system_node_module_category, account: other_account)
        foreign_module = create(:system_node_module, account: other_account,
                                node_platform: foreign_platform, category: foreign_cat,
                                variety: "subscription", name: "foreign")

        post "/api/v1/system/node_modules/#{foreign_module.id}/rollback", headers: headers

        expect(response).to have_http_status(:not_found)
      end
    end

    context "no previous version" do
      it "422s when the module has no history to roll back to" do
        lone = create(:system_node_module, account: account, node_platform: platform,
                      category: category, variety: "subscription", name: "lone-mod")
        # Single version + no prior — rollback_to_previous raises RollbackError
        only = create(:system_node_module_version, node_module: lone, version_number: 1)
        lone.update_columns(current_version_id: only.id, current_version_number: 1)

        post "/api/v1/system/node_modules/#{lone.id}/rollback", headers: headers

        expect(response).to have_http_status(:unprocessable_content)
        expect(JSON.parse(response.body)["error"]).to match(/previous|rollback/i)
      end
    end

    context "permissions" do
      it "403s when the user lacks system.modules.update" do
        viewer = user_with_permissions("system.modules.read", account: account)
        viewer_headers = auth_headers_for(viewer).merge("Content-Type" => "application/json")

        post "/api/v1/system/node_modules/#{node_module.id}/rollback", headers: viewer_headers

        expect(response).to have_http_status(:forbidden)
      end
    end
  end
end
