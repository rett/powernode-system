# frozen_string_literal: true

require "rails_helper"

# Golden Eclipse Block BB3 — Mark/unmark module as honeypot canary.
RSpec.describe "Module canary marking", type: :request do
  let(:account)  { create(:account) }
  let(:platform) { create(:system_node_platform, account: account) }
  let(:category) { create(:system_node_module_category, account: account) }
  let(:mod) do
    create(:system_node_module, account: account, node_platform: platform,
           category: category, variety: "subscription", name: "test-mod")
  end

  let(:operator) { user_with_permissions("system.modules.update", account: account) }
  let(:reader)   { user_with_permissions("system.modules.read", account: account) }

  describe "POST /api/v1/system/node_modules/:id/mark_canary" do
    it "marks the module as a canary with default lure_kind" do
      post "/api/v1/system/node_modules/#{mod.id}/mark_canary",
           headers: auth_headers_for(operator)
      expect(response).to have_http_status(:ok)
      mod.reload
      expect(mod.config["honeypot"]["canary"]).to be true
      expect(mod.config["honeypot"]["lure_kind"]).to eq("credential_store")
    end

    it "honors a custom lure_kind" do
      post "/api/v1/system/node_modules/#{mod.id}/mark_canary",
           params: { lure_kind: "admin_shell" }.to_json,
           headers: auth_headers_for(operator).merge("Content-Type" => "application/json")
      expect(mod.reload.config["honeypot"]["lure_kind"]).to eq("admin_shell")
    end

    it "is idempotent — re-marking doesn't change marked_at" do
      post "/api/v1/system/node_modules/#{mod.id}/mark_canary",
           headers: auth_headers_for(operator)
      original_marked_at = mod.reload.config["honeypot"]["marked_at"]

      post "/api/v1/system/node_modules/#{mod.id}/mark_canary",
           headers: auth_headers_for(operator)
      expect(mod.reload.config["honeypot"]["marked_at"]).to eq(original_marked_at)
    end

    it "rejects when caller lacks system.modules.update" do
      post "/api/v1/system/node_modules/#{mod.id}/mark_canary",
           headers: auth_headers_for(reader)
      expect(response.status).to be_in([ 401, 403 ])
    end
  end

  describe "POST /api/v1/system/node_modules/:id/unmark_canary" do
    it "clears the honeypot config block" do
      System::Honeypot::CanaryModuleService.mark!(node_module: mod)
      expect(mod.reload.config["honeypot"]["canary"]).to be true

      post "/api/v1/system/node_modules/#{mod.id}/unmark_canary",
           headers: auth_headers_for(operator)
      expect(response).to have_http_status(:ok)
      expect(mod.reload.config["honeypot"]).to be_nil
    end

    it "is a no-op when module isn't a canary" do
      post "/api/v1/system/node_modules/#{mod.id}/unmark_canary",
           headers: auth_headers_for(operator)
      expect(response).to have_http_status(:ok)
      expect(mod.reload.config["honeypot"]).to be_nil
    end
  end
end
