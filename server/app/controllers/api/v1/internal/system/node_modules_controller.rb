# frozen_string_literal: true

module Api
  module V1
    module Internal
      module System
        # Internal API controller for system node module operations
        # Handles module build and commit operations
        class NodeModulesController < BaseController
          before_action :set_node_module, only: %i[show build commit]

          # GET /api/v1/internal/system/node_modules/:id
          def show
            render_success(data: module_data(@node_module))
          end

          # POST /api/v1/internal/system/node_modules/:id/build
          # Build module on instance (install packages, capture file spec)
          def build
            node_instance_id = params[:node_instance_id]
            operation_id = params[:operation_id]

            unless node_instance_id.present?
              return render_error("node_instance_id is required", status: :unprocessable_entity)
            end

            instance = ::System::NodeInstance.find_by(id: node_instance_id)
            unless instance
              return render_error("Node instance not found", status: :not_found)
            end

            # Validate package spec exists
            unless @node_module.package_spec.present?
              return render_error("Module has no package specification", status: :unprocessable_entity)
            end

            result = ::System::ModuleBuildService.build(
              node_module: @node_module,
              options: { operation_id: operation_id }
            )

            if result.success?
              render_success(
                data: {
                  success: true,
                  module_id: @node_module.id,
                  build_id: result.data[:build_id],
                  artifacts: result.data[:artifacts]
                }
              )
            else
              render_success(
                data: {
                  success: false,
                  error: result.error,
                  stage: result.data[:stage]
                }
              )
            end
          rescue StandardError => e
            Rails.logger.error("[System::NodeModules] Build failed: #{e.message}")
            render_error(e.message, status: :internal_server_error)
          end

          # POST /api/v1/internal/system/node_modules/:id/commit
          # Commit module from instance (rsync files, create squashfs, upload)
          def commit
            node_instance_id = params[:node_instance_id]
            operation_id = params[:operation_id]

            unless node_instance_id.present?
              return render_error("node_instance_id is required", status: :unprocessable_entity)
            end

            instance = ::System::NodeInstance.find_by(id: node_instance_id)
            unless instance
              return render_error("Node instance not found", status: :not_found)
            end

            # Validate rsync spec exists
            unless @node_module.rsync_spec.present?
              return render_error("Module has no rsync specification", status: :unprocessable_entity)
            end

            result = ::System::ModuleCommitService.commit(
              node_module: @node_module,
              instance: instance,
              options: { operation_id: operation_id }
            )

            if result.success?
              render_success(
                data: {
                  success: true,
                  module_id: @node_module.id,
                  commit_id: result.data[:commit_id],
                  duration: result.data[:duration]
                }
              )
            else
              render_success(
                data: {
                  success: false,
                  error: result.error,
                  stage: result.data[:stage]
                }
              )
            end
          rescue StandardError => e
            Rails.logger.error("[System::NodeModules] Commit failed: #{e.message}")
            render_error(e.message, status: :internal_server_error)
          end

          private

          def set_node_module
            @node_module = ::System::NodeModule.find(params[:id])
          rescue ActiveRecord::RecordNotFound
            render_not_found("NodeModule")
          end

          def module_data(node_module)
            {
              id: node_module.id,
              name: node_module.name,
              description: node_module.description,
              variety: node_module.variety,
              enabled: node_module.enabled,
              public: node_module.public,
              priority: node_module.priority,
              node_platform_id: node_module.node_platform_id,
              category_id: node_module.node_module_category_id,
              package_spec: node_module.package_spec,
              rsync_spec: node_module.rsync_spec,
              file_spec: node_module.file_spec,
              mask: node_module.mask,
              lock_spec: node_module.lock_spec,
              build_script_id: node_module.build_script_id,
              has_dependencies: node_module.has_dependencies?,
              created_at: node_module.created_at,
              updated_at: node_module.updated_at
            }
          end
        end
      end
    end
  end
end
