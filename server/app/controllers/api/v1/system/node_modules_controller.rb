# frozen_string_literal: true

module Api
  module V1
    module System
      class NodeModulesController < BaseController
        before_action :set_node_module, only: [:show, :update, :destroy, :dependencies, :mark_canary, :unmark_canary]

        # GET /api/v1/system/node_modules
        def index
          require_permission('system.modules.read')

          modules = current_account.system_node_modules
          modules = apply_filters(modules)
          modules = paginate(modules.includes(:category, :node_platform, :copy_path).by_priority)

          render_success(
            node_modules: modules.map { |m| ::System::NodeModuleSerializer.new(m).as_json },
            meta: pagination_meta
          )
        end

        # GET /api/v1/system/node_modules/:id
        def show
          require_permission('system.modules.read')
          render_success(node_module: ::System::NodeModuleSerializer.new(@node_module).as_json)
        end

        # POST /api/v1/system/node_modules
        def create
          require_permission('system.modules.create')

          node_module = current_account.system_node_modules.build(node_module_params)

          if node_module.save
            render_success(node_module: ::System::NodeModuleSerializer.new(node_module).as_json, status: :created)
          else
            render_validation_error(node_module)
          end
        end

        # PATCH/PUT /api/v1/system/node_modules/:id
        def update
          require_permission('system.modules.update')

          if @node_module.update(node_module_params)
            render_success(node_module: ::System::NodeModuleSerializer.new(@node_module).as_json)
          else
            render_validation_error(@node_module)
          end
        end

        # DELETE /api/v1/system/node_modules/:id
        def destroy
          require_permission('system.modules.delete')

          if @node_module.has_dependents?
            render_error('Cannot delete module that other modules depend on', status: :unprocessable_entity)
          else
            @node_module.destroy
            render_success(message: 'Module deleted successfully')
          end
        end

        # GET /api/v1/system/node_modules/:id/dependencies
        def dependencies
          require_permission('system.modules.read')

          render_success(
            dependencies: @node_module.dependencies.map { |d| ::System::NodeModuleSerializer.new(d).as_json },
            dependents: @node_module.dependents.map { |d| ::System::NodeModuleSerializer.new(d).as_json }
          )
        end

        # POST /api/v1/system/node_modules/:id/mark_canary
        # Marks this module as a honeypot canary (Track F-6). Any access
        # via canonical canary observation paths emits a high-severity
        # FleetEvent that surfaces in the dashboard + routes through autonomy.
        def mark_canary
          require_permission('system.modules.update')

          # Rails wraps params under :node_module due to wrap_parameters; check both.
          lure = params[:lure_kind].presence ||
                 params.dig(:node_module, :lure_kind).presence ||
                 'credential_store'

          ok = ::System::Honeypot::CanaryModuleService.mark!(
            node_module: @node_module,
            lure_kind: lure
          )
          if ok
            render_success(node_module: ::System::NodeModuleSerializer.new(@node_module.reload).as_json)
          else
            render_error('Failed to mark module as canary', 422)
          end
        end

        # POST /api/v1/system/node_modules/:id/unmark_canary
        # Removes the honeypot canary marker by clearing config['honeypot'].
        def unmark_canary
          require_permission('system.modules.update')

          if @node_module.config&.dig('honeypot')
            new_config = @node_module.config.deep_dup
            new_config.delete('honeypot')
            @node_module.update!(config: new_config)
          end
          render_success(node_module: ::System::NodeModuleSerializer.new(@node_module.reload).as_json)
        end

        private

        def set_node_module
          @node_module = current_account.system_node_modules.find(params[:id])
        end

        def node_module_params
          # Spec fields (mask, file_spec, package_spec, dependency_spec,
          # protected_spec) accept EITHER a scalar string (newline-joined
          # glob lines, the form-friendly shape — the model's encode_specs
          # before_validation callback converts to base64-encoded arrays
          # on save) OR an Array of already-encoded base64 strings (the
          # programmatic shape — kept as-is by encode_specs).
          #
          # strong_params forces a single shape per key, so we permit the
          # scalar form and post-process arrays manually below.
          permitted = params.require(:node_module).permit(
            :name, :description, :variety, :enabled, :public, :priority,
            :node_platform_id, :category_id, :copy_path_id,
            # Lifecycle / lock state
            :lock_spec, :init_start, :init_stop, :init_restart, :reboot_required,
            # Spec fields as scalar (textarea / newline-joined strings)
            :mask, :file_spec, :package_spec, :dependency_spec, :protected_spec,
            # Golden Eclipse Block R — per-module autonomy controls.
            :consent_budget_per_day, :consent_budget_used_count,
            :consent_budget_window_start_at,
            config: {}
          )

          # Re-admit the spec fields when they arrive as Arrays (already
          # encoded). strong_params would otherwise drop them, since the
          # scalar permit above only matches scalar values.
          %i[mask file_spec package_spec dependency_spec protected_spec].each do |field|
            raw = params[:node_module][field]
            permitted[field] = raw if raw.is_a?(Array)
          end

          permitted
        end

        def apply_filters(modules)
          modules = modules.enabled if params[:enabled] == 'true'
          modules = modules.disabled if params[:enabled] == 'false'
          modules = modules.public_modules if params[:public] == 'true'
          modules = modules.by_variety(params[:variety]) if params[:variety].present?
          modules = modules.for_platform(params[:platform_id]) if params[:platform_id].present?
          modules = modules.in_category(params[:category_id]) if params[:category_id].present?
          modules = modules.where('name ILIKE ?', "%#{params[:search]}%") if params[:search].present?
          modules
        end
      end
    end
  end
end
