# Adds `protected_spec` to System::NodeModule (and its versions) — the
# fifth rsync-glob spec field, distinct from `mask`.
#
# Semantic split:
#   - mask: paths I exclude from MY OWN blob during the build (the legacy
#     rsync local-exclude). Used for build cruft (/var/cache/apt, doc/man,
#     /var/log) that the package install creates but the module shouldn't
#     ship. Does NOT cross module boundaries.
#   - protected_spec: paths I CLAIM. Flows in BOTH priority directions
#     into every neighbor's effective_mask, so no neighbor's blob may
#     ship the claimed paths. Used for sensitive paths the module owns
#     (/etc/shadow, /etc/ssh/ssh_host_*_key, /etc/sudoers) where union-
#     mount overrides would be a security regression.
#
# The two fields can overlap if a module wants both effects: exclude X
# from my blob AND prevent any neighbor from shipping X. But typically
# the claimer wants to SHIP its protected paths (system-base ships
# /etc/shadow), so protected_spec stands alone.
#
# Storage matches the four existing spec columns: jsonb array of
# base64-encoded glob lines, default [].
class AddProtectedSpecToNodeModules < ActiveRecord::Migration[8.0]
  def change
    add_column :system_node_modules,         :protected_spec, :jsonb, null: false, default: []
    add_column :system_node_module_versions, :protected_spec, :jsonb, null: false, default: []

    add_index :system_node_modules,         :protected_spec, using: :gin
    add_index :system_node_module_versions, :protected_spec, using: :gin
  end
end
